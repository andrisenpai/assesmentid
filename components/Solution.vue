<script lang="js">
    export default {
        mounted(){
              const buttons = document.querySelectorAll('.fs-button');
              buttons.forEach(button => {
                button.addEventListener('click', function(e) {
                  e.preventDefault();
                  filterSolution(button.getAttribute('data-target'));
                });
              });
          
              const filterSolution = (solution) => {
                //for add/remove active from button 
                buttons.forEach(button => {
                  const target = button.getAttribute('data-target');
                  if (target === solution) {
                    button.classList.add('active');
                  } else {
                    button.classList.remove('active');
                  }
                });

                if (solution === 'all') {
                  document.querySelectorAll('.fs').forEach(fs => fs.style.display = 'block');
                } else {
                  document.querySelectorAll('.fs').forEach(fs => {
                    if (fs.classList.contains(`fs-${solution}`)) {
                      fs.style.display = 'block';
                    } else {
                      fs.style.display = 'none';
                    }
                  });
                }
              };
            }
        }

</script>

